export const mockPerfumes = unsortedPerfumes.sort((a, b) => a.name.localeCompare(b.name));

export function getBrands(): string[] {
  const brands = new Set(mockPerfumes.map(perfume => perfume.brand));
  return Array.from(brands).sort();
}

export const getPerfumeBySlug = (slug: string): Perfume | undefined => {
  return mockPerfumes.find(p => p.slug === slug);
};

export const getRecommendations = (perfume: Perfume, limit: number = 4): Perfume[] => {
  // Calculate weighted scores based on main accord priority (mainaccord1 to mainaccord5)
  const scoredPerfumes = mockPerfumes
    .filter(p => p.id !== perfume.id) // Exclude the original perfume
    .filter(p => p.gender === perfume.gender) // Same gender only
    .map(p => {
      let score = 0;
      
      // Weighted scoring based on main accord priority
      // mainaccord1 (index 0) = highest priority (5 points)
      // mainaccord2 (index 1) = high priority (4 points)
      // mainaccord3 (index 2) = medium priority (3 points)
      // mainaccord4 (index 3) = low priority (2 points)
      // mainaccord5 (index 4) = lowest priority (1 point)
      
      for (let i = 0; i < perfume.notes.length; i++) {
        const perfumeNote = perfume.notes[i];
        const priority = perfume.notes.length - i; // 5, 4, 3, 2, 1 for mainaccord1-5
        
        if (p.notes.includes(perfumeNote)) {
          score += priority;
        }
      }
      
      return { perfume: p, score };
    })
    .filter(item => item.score > 0) // Only include perfumes with at least one shared note
    .sort((a, b) => b.score - a.score); // Sort by score (highest first)
  
  // If we have enough recommendations with shared notes, return them
  if (scoredPerfumes.length >= limit) {
    return scoredPerfumes.slice(0, limit).map(item => item.perfume);
  }
  
  // If not enough, fill with other same-gender perfumes (excluding already picked ones)
  const alreadyIds = new Set(scoredPerfumes.map(item => item.perfume.id));
  const fill = mockPerfumes
    .filter(p => p.id !== perfume.id)
    .filter(p => p.gender === perfume.gender)
    .filter(p => !alreadyIds.has(p.id));
  
  return [...scoredPerfumes.map(item => item.perfume), ...fill].slice(0, limit);
};